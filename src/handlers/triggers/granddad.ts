export async function granddad(ctx: Context) {
	let answer = random([...answers, ...SLURS]);

	if (probability(10) && !endsWithPunctuation(answer)) {
		answer += random(["!", "..."]);
	}

	if (probability(10)) {
		answer += ` ${random(EMOJI)}`;
	}

	if (probability(10)) {
		answer = answer.toUpperCase();
	}

	await ctx.reply(answer);
}
